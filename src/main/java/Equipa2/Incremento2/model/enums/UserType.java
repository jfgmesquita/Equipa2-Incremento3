package Equipa2.Incremento2.model.enums;

/**
 * A enumeração UserType representa os diferentes tipos de utilizadores.
 */
public enum UserType {

    /**
     * Indica que o utilizador é um cliente.
     */
    CLIENTE,

    /**
     * Indica que o utilizador é um profissional.
     */
    PROFISSIONAL,

    /**
     * Indica que o utilizador é um administrador.
     */
    ADMINISTRADOR
    
}
